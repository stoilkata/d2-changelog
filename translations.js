function translateSectionHtmlToBg(html) {
    let result = html;

    for (const [from, to] of bgSectionReplacements) {
        result = result.split(from).join(to);
    }

    for (const [pattern, replacement] of bgGenericReplacements) {
        result = result.replace(pattern, replacement);
    }

    return result;
}

// Language management functions
class LanguageManager {
    constructor() {
        this.currentLanguage = localStorage.getItem('language') || 'en';
        this.translations = translations;
        this.originalSectionContent = {};
    }
    
    // Get translation for a specific key
    t(key) {
        const keys = key.split('.');
        let value = this.translations[this.currentLanguage];
        
        for (const k of keys) {
            if (value && value[k]) {
                value = value[k];
            } else {
                // Fallback to English if key not found
                value = this.translations['en'];
                for (const k of keys) {
                    if (value && value[k]) {
                        value = value[k];
                    } else {
                        return key; // Return key if not found in English either
                    }
                }
                break;
            }
        }
        
        return value || key;
    }
    
    // Switch language
    setLanguage(lang) {
        if (this.translations[lang]) {
            this.currentLanguage = lang;
            localStorage.setItem('language', lang);
            this.updatePageTranslations();
            this.updateLanguageSelector();
        }
    }
    
    // Get current language
    getCurrentLanguage() {
        return this.currentLanguage;
    }
    
    // Update all translations on the page
    updatePageTranslations() {
        // Update all elements with data-translate attribute
        document.querySelectorAll('[data-translate]').forEach(element => {
            const key = element.getAttribute('data-translate');
            const translation = this.t(key);
            
            if (element.tagName === 'INPUT' && element.type === 'submit') {
                element.value = translation;
            } else if (element.tagName === 'INPUT' && element.hasAttribute('placeholder')) {
                element.placeholder = translation;
            } else {
                element.textContent = translation;
            }
        });
        
        // Update HTML lang attribute
        document.documentElement.lang = this.currentLanguage === 'bg' ? 'bg' : 'en';

        // Swap complex section content
        document.querySelectorAll('[data-section-content]').forEach(section => {
            const key = section.getAttribute('data-section-content');
            if (!this.originalSectionContent[key]) {
                this.originalSectionContent[key] = section.innerHTML;
            }

            if (this.currentLanguage === 'bg') {
                section.innerHTML = translateSectionHtmlToBg(this.originalSectionContent[key]);
            } else {
                section.innerHTML = this.originalSectionContent[key];
            }
        });
    }
    
    // Update language selector UI
    updateLanguageSelector() {
        const languageButtons = document.querySelectorAll('.language-btn');
        languageButtons.forEach(btn => {
            btn.classList.remove('active');
            if (btn.getAttribute('data-lang') === this.currentLanguage) {
                btn.classList.add('active');
            }
        });
    }
    
    // Initialize translations when DOM is loaded
    init() {
        document.querySelectorAll('[data-section-content]').forEach(section => {
            const key = section.getAttribute('data-section-content');
            if (!this.originalSectionContent[key]) {
                this.originalSectionContent[key] = section.innerHTML;
            }
        });

        this.updatePageTranslations();
        this.updateLanguageSelector();
        
        // Add event listeners for language buttons
        document.querySelectorAll('.language-btn').forEach(btn => {
            btn.addEventListener('click', (e) => {
                e.preventDefault();
                const lang = btn.getAttribute('data-lang');
                this.setLanguage(lang);
            });
        });
    }
}

